# Guía para descargar y abrir cursosAct desde GitHub

Este documento explica, en estilo de **R Markdown**, cómo descargar el archivo `cursosAct.zip` desde GitHub, descomprimirlo y abrirlo en **Visual Studio Code** o en **Google Chrome** para visualizar el sistema de gestión de cursos.

---
  
  ##  1. Descargar el archivo ZIP desde GitHub
  
  ```R
# Paso 1: Ve al repositorio en GitHub donde está el proyecto.
# Ejemplo de URL:
# https://github.com/NeryLopez2/Programaci-n-Avanzada.git

# Paso 2: Haz clic en el botón verde "Code".
# Paso 3: Selecciona "Download ZIP".
# Esto descargará el archivo cursosAct.zip en tu carpeta Descargas.

# Una vez descargado, localiza el archivo "cursosAct.zip".
# Con clic derecho selecciona:
#   - En Windows: "Extraer todo..."
#   - En Mac/Linux: "Extraer aquí".

# Para visualizar el sistema:
# 1. Dentro de la carpeta "cursosAct", busca el archivo "index.html".
# 2. Haz doble clic en él.
# 3. Se abrirá en tu navegador predeterminado.
# 4. Para usar Chrome:
#    - Haz clic derecho en index.html -> "Abrir con" -> "Google Chrome".



# Se creará una carpeta llamada "cursosAct" con todos los archivos del proyecto.

# 1. Abre Visual Studio Code.
# 2. Menú: Archivo -> Abrir carpeta.
# 3. Selecciona la carpeta "cursosAct".
# 4. Ahora puedes ver y editar archivos como index.html, estilos.css y script.js.

